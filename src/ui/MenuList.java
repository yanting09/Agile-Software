    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        //Add Button

        int yesNo = JOptionPane.showConfirmDialog(null, "Confirm add the following item into menu?", "Add menu item", JOptionPane.YES_NO_OPTION);
        if (yesNo == 0) {
            try{
            String menuCode = jTextField2.getText();
            String itemName = jTextField4.getText();
            String price = jTextField3.getText();
            if (jTextField2.getText().charAt(0) == 'F') {
                DefaultTableModel model1 = (DefaultTableModel) jTable2.getModel();
                jTextField2.setText(menuCode);
                jTextField4.setText(itemName);
                jTextField3.setText(price);
                model1.addRow(new Object[]{menuCode, itemName, price});
                jTextField2.setText("");
                jTextField3.setText("");
                jTextField4.setText("");
            } else if (jTextField2.getText().charAt(0) == 'D') {
                DefaultTableModel model2 = (DefaultTableModel) jTable1.getModel();
                jTextField2.setText(menuCode);
                jTextField4.setText(itemName);
                jTextField3.setText(price);
                model2.addRow(new Object[]{menuCode, itemName, price});
                jTextField2.setText("");
                jTextField3.setText("");
                jTextField4.setText("");
            }
             JOptionPane.showMessageDialog(null, "Add menu item successfully !","Success",JOptionPane.INFORMATION_MESSAGE);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Please fill in the field to add menu item","Error",JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_jButton4ActionPerformed

