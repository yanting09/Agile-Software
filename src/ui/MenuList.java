    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
//Update Button

        int yesNo = JOptionPane.showConfirmDialog(null, "Confirm update the following item from menu?", "Update menu", JOptionPane.YES_NO_OPTION);
        if (yesNo == 0) {
            try {
                if (jTextField2.getText().charAt(0) == 'F') {
                    int i = jTable2.getSelectedRow();
                    i = jTable2.convertRowIndexToModel(i);
                    DefaultTableModel model1 = (DefaultTableModel) jTable2.getModel();
                    if (i >= 0) {
                        model1.setValueAt(jTextField2.getText(), i, 0);
                        model1.setValueAt(jTextField4.getText(), i, 1);
                        model1.setValueAt(jTextField3.getText(), i, 2);
                        jTextField2.setText("");
                        jTextField3.setText("");
                        jTextField4.setText("");
                    }
                } else if (jTextField2.getText().charAt(0) == 'D') {
                    int j = jTable1.getSelectedRow();
                    j = jTable1.convertRowIndexToModel(j);
                    DefaultTableModel model2 = (DefaultTableModel) jTable1.getModel();
                    if (j >= 0) {
                        model2.setValueAt(jTextField2.getText(), j, 0);
                        model2.setValueAt(jTextField4.getText(), j, 1);
                        model2.setValueAt(jTextField3.getText(), j, 2);
                        jTextField2.setText("");
                        jTextField3.setText("");
                        jTextField4.setText("");
                    }
                }
                 JOptionPane.showMessageDialog(null, "Update menu successfully !","Success",JOptionPane.INFORMATION_MESSAGE);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Please fill in the field to update menu","Error",JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_jButton5ActionPerformed

